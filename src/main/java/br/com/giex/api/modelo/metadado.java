package br.com.giex.api.modelo;

/**
 *
 * @author jjunior
 */
public class metadado {
    
    private String chave;
    private String valor;

    public metadado() {
    }

    public metadado(String chave, String valor) {
        this.chave = chave;
        this.valor = valor;
    }

    public String getChave() {
        return chave;
    }

    public void setChave(String chave) {
        this.chave = chave;
    }

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }
}